    private void applyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_applyButtonActionPerformed
        int res = FACADE.upload(path, jTextField4.getText(),jTextField2.getText() , jTextField1.getText());
        this.dispose();  
        new MyCollectionFrame().setVisible(true);
        
        switch (res) {
            case 0:
                new ErrorFrame("Conteudo adicionado com sucesso.");
                break;
            case 1:
                new ErrorFrame("Um conteudo co messa nome ja esta na app, foi adicionado a sua colecao.");
                break;
            case 2:
                new ErrorFrame("Um conteudo co messa nome ja esta na sua collecao.");
                break;
            case 3:
                new ErrorFrame("Erro ao carregar o ficheiro.");
                break;
            default:
                break;
        }
    }//GEN-LAST:event_applyButtonActionPerformed

